package curso_programacao;

public class Processamento_dados_Casting {

	public static void main(String[] args) {
		int x,y;
		
		x=20;
		y=40*x;
	System.out.println("O resultado da sua multiplicação é " + y);

	// Calculando o trapézio //
	
	double b, B, h, div, area;
	
	b = 6.0;
	B = 8.0;
	h = 5.0;
	div = 2.0;
	
	area = (b+B)/div*h;
	
	System.out.printf("A área calculada do seu trapézio é de %.0f", area);
	
	}

}
